package Inheritance.Ex3;

public class Employee {
    private double salary;
    private String work;

    public void setSalary(double salary){
        this.salary=salary;
    }


    public  double getSalary(){
        return this.salary;
    }


    public void setWork(String work){
        this.work=work;
    }
    public   String getWork(){
        return this.work;
    }


}
